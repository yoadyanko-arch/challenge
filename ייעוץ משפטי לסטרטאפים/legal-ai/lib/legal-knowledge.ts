/**
 * מחזיר chunks רלוונטיים לשאלה לפי מילות מפתח
 */
export function retrieveRelevantKnowledge(query: string): KnowledgeChunk[] {
  const queryLower = query.toLowerCase();

  const scored = LEGAL_KNOWLEDGE.map((chunk) => {
    let score = 0;
    for (const kw of chunk.keywords) {
      if (queryLower.includes(kw.toLowerCase())) {
        score += 2;
      }
    }
    // בדיקת נושא כללי
    if (queryLower.includes(chunk.topic.toLowerCase())) {
      score += 3;
    }
    return { chunk, score };
  });

  return scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 2) // מחזיר עד 2 chunks הרלוונטיים ביותר
    .map((s) => s.chunk);
}
